#负责客观总结：论文做了什么？ 方法是什么？ 实验是什么？ 结论是什么
class ReaderNotes(BaseModel):
  """
    Output of Reader Agent.

    Reader agent should focus on faithful extraction and summarization.
    It should not perform deep criticism here.
  """

  problem_statement: str = Field(default="")
  background: str = Field(default="")
  main_contributions: list[str] = Field(default_factory=list)
  method_summary: str = Field(default="")
  experiment_summary: str = Field(default="")
  conclusion_summary: str = Field(default="")
  key_terms: list[str] = Field(default_factory=list)
  important_evidence_ids: list[str] = Field(default_factory=list)

  @field_validator(
    "problem_statement",
    "background",
    "method_summary",
    "experiment_summary",
    "conclusion_summary"
  )
  @classmethod
  def strip_text(cls, value: str)-> str:
    return value.strip()
  
  @field_validator("main_contributions", "key_terms", "important_evidence_ids")
  @classmethod
  def clean_string_list(cls, values: list[str]) -> list[str]:
    cleaned = []
    for item in values:
      item = item.strip()
      if item:
        cleaned.append(item)
    return cleaned
  

