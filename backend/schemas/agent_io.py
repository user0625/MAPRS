from __future__ import annotations

from enum import Enum
from typing import Literal

from pydantic import BaseModel, ConfigDict, Field, field_validator, model_validator

from backend.schemas.paper import PaperChunk, PaperMetadata

class AnalysisMode(str, Enum):
  """Supported analysis modes"""

  SINGLE_PAPER_ANALYSIS = "single_paper_analysis"
  PAPER_COMPARISON = "paper_comparison"
  ASK_THE_PAPER = "ask_the_paper"

class AgentRole(str, Enum):
  """Agent roles used in the system"""

  PLANNER = "planner"
  READER = "reader"
  RAG = "rag"
  CRITIC = "critic"
  WRITER = "writer"

class AgentTask(BaseModel):
  """
    A single task generated by the Planner Agent.

    Example:
    - extract_contributions
    - analyze_method
    - summarize_experiments
  """

  task_id: str = Field(..., min_length=1)
  name: str = Field(..., min_length=1)
  description: str = Field(default="")
  assigned_to: AgentRole | None = Field(default=None)
  priority: int = Field(default=1, ge=1, le=5)

  model_config = ConfigDict(use_enum_values=True)

  @field_validator("task_id", "name", "description")
  @classmethod
  def strip_text(cls, value: str) -> str:
    return value.strip()
  

class AnalysisPlan(BaseModel):
  """
    output of planner agent.

    It tells the system what to analyze and which focus questions should guide later agents.
  """

  model_config = ConfigDict(use_enum_values=True)

  mode: AnalysisMode = Field(default=AnalysisMode.SINGLE_PAPER_ANALYSIS)
  tasks: list[AgentTask] = Field(default_factory=list)
  focus_questions: list[str] = Field(default_factory=list)
  required_sections: list[str] = Field(default_factory=list)
  need_retrieval: bool = Field(default=True)
  notes: str|None = Field(default=None)

  @field_validator("focus_questions", "required_sections")
  @classmethod
  def clean_string_list(cls, values: list[str]) -> list[str]:
    cleaned = []
    for item in values:
      item = item.strip()
      if item:
        cleaned.append(item)
    return cleaned
  
  @field_validator("notes")
  @classmethod
  def strip_optional_text(cls, value: str|None) -> str|None :
    if value is None:
      return None
    value = value.strip()
    return value or None
  

#RAG的桥梁，连接PaperChunk -> Retriever -> Reader/Critic/Writer
class EvidenceItem(BaseModel):
  """
    A single piece of evidence retrieved from chunks.

    This object is designed to be cited by Reader / Critic / Writer outputs.
  """

  evidence_id: str = Field(..., min_length=1)
  query: str|None = Field(default=None)

  chunk_id: str = Field(..., min_length=1)
  paper_id: str|None = Field(default=None)

  text: str = Field(..., min_length=1)
  score: float|None = Field(default=None)

  page_start: int|None = Field(default=None, ge=1)
  page_end: int|None = Field(default=None, ge=1)
  section: str|None = Field(default=None)

  @field_validator("query", "paper_id", "section")
  @classmethod
  def strip_optional_text(cls, value: str|None) -> str|None:
    if value is None:
      return None
    value = value.strip()
    return value or None
  
  @model_validator(mode="after")
  def validate_page_range(self) -> "EvidenceItem":
    if self.page_start is not None and self.page_end is not None:
      if self.page_end < self.page_start:
        raise ValueError("Page end must be grater than or euqal to page start")
    return self
  
  @classmethod
  def from_chunk(cls, chunk: PaperChunk, evidence_id: str, query: str|None = None, score: float|None = None) -> "EvidenceItem":
    """
      Create an EvidenceItem from a paperchunk.
      This helper is useful in retriever.py
    """

    return cls(
      evidence_id = evidence_id,
      query = query,
      chunk_id = chunk.chunk_id,
      paper_id = chunk.paper_id,
      text = chunk.text,
      score = score,
      page_start = chunk.page_start,
      page_end = chunk.page_end,
      section = chunk.section,
    )
  

class EvidenceBundle(BaseModel):
  """
    A group of retrieved evidence items.
    Usually generated by retriever.py or RAG Agent.
  """

  query_list: list[str] = Field(default_factory=list)
  items: list[EvidenceItem] = Field(default_factory=list)

  @field_validator("query_list")
  @classmethod
  def clean_query_list(cls, values: list[str]) -> list[str]:
    cleaned = []
    for item in values:
      item = item.strip()
      if item:
        cleaned.append(item)
    return cleaned
  
  def top_k(self, k: int) -> list[EvidenceItem]:
    """Return top-k evidence items"""
    return self.items[:k]
  
  def evidence_ids(self) -> list[str]:
    """return all evidence IDs"""
    return [item.evidence_id for item in self.items]
  


class PlannerInput(BaseModel):
  """input of planner agent"""

  paper_metadata: PaperMetadata
  user_query: str = Field(
    default="分析这篇论文然后生成结构化的阅读报告."
  )

  @field_validator("user_query")
  @classmethod
  def strip_user_query(cls, value: str) -> str:
    value = value.strip()
    if not value:
      raise ValueError("user_query cannot bu empty")
    return value
  


class ReaderInput(BaseModel):
  """Input of Reader Agent"""

  paper_metadata: PaperMetadata
  chunks: list[PaperChunk] = Field(default_factory=list)
  analysis_plan: AnalysisPlan
  evidence_bundle:EvidenceBundle|None = None

  @model_validator(mode="after")
  def validate_context(self) -> "ReaderInput":
    has_chunks = bool(self.chunks)
    has_evidence = self.evidence_bundle is not None and bool(self.evidence_bundle.items)
    if not has_chunks and not has_evidence:
      raise ValueError("ReaderInput requires either chunks or evidence_bundle.")
    return self
